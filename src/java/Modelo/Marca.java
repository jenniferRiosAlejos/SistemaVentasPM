package Modelo;

/**
 *
 * @author enriq
 */
public class Marca {
    int id;
    String descrip;
    String estado;

    public Marca() {
    }

    public Marca(int id, String descrip, String estado) {
        this.id = id;
        this.descrip = descrip;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescrip() {
        return descrip;
    }

    public void setDescrip(String descrip) {
        this.descrip = descrip;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
    
    
    
    
    
}
